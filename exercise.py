### Задача 6: Классификация числа по диапазону
#Описание:Запросите у пользователя число и выведите, входит ли оно в один из диапазонов:
#- От 1 до 10 - От 11 до 20
#- От 21 до 30  - Выведите сообщение "Число вне диапазона", если оно не попадает ни в один из указанных.


number = int(input("Введите число: "))

if 1 <= number <= 10:
    print("Число в диапазоне от 1 до 10.")
elif 11 <= number <= 20:
    print("Число в диапазоне от 11 до 20.")
elif 21 <= number <= 30:
    print("Число в диапазоне от 21 до 30.")
else:
    print("Число вне диапазона.")


### Задача 7: Рассмотрение нескольких условий
#Описание:Напишите программу, которая запрашивает у пользователя число и выводит следующее:
#- "Положительное четное" - "Положительное нечетное"
#- "Отрицательное четное" - "Отрицательное нечетное" - "Ноль"


number = int(input("Введите целое число: "))

if number > 0:
    if number % 2 == 0:
        print("Положительное четное")
    else:
        print("Положительное нечетное")
elif number < 0:
    if number % 2 == 0:
        print("Отрицательное четное")
    else:
        print("Отрицательное нечетное")
else:
    print("Ноль")


