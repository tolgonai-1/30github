### Задача 4: Определение возраста
#Описание:Напишите программу, которая запрашивает у пользователя возраст и выводит соответствующее сообщение.
#- Если возраст меньше 13: "Вы ребенок." - Если возраст от 13 до 19: "Вы подросток."
#- Если возраст от 20 до 64: "Вы взрослый." - Если возраст 65 и старше: "Вы пожилой человек."


age = int(input("Введите ваш возраст: "))

if age < 13:
    print("Вы ребенок.")
elif 13 <= age <= 19:
    print("Вы подросток.")
elif 20 <= age <= 64:
    print("Вы взрослый.")
else:
    print("Вы пожилой человек.")

