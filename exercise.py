### Задача 2: Оценка по баллам
#Описание:Напишите программу, которая запрашивает у пользователя количество баллов и выдает оценку по следующей системе:
#- 90-100: A - 80-89: B- 70-79: C- 60-69: D- ниже 60: F


score = int(input("Введите количество баллов: "))

if score >= 90:
    print("Ваша оценка: A")
elif score >= 80:
    print("Ваша оценка: B")
elif score >= 70:
    print("Ваша оценка: C")
elif score >= 60:
    print("Ваша оценка: D")
else:
    print("Ваша оценка: F")


### Задача 3: Определение времён года
